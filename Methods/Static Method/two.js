class Maths{
    static add(a,b){
        return a+b
    }
    static sub(a,b){
        return a-b
    }
    static mul(a,b){
        return a*b
    }
    static div(a,b){
        return a/b
    }
}

console.log(Maths.add(10,20))
console.log(Maths.sub(20,10))
console.log(Maths.mul(20,10))
console.log(Maths.div(20,10))